#include "libft.h"
char	*ft_strchr(const char *s, int c)
{
	if (*s == '\0')
	{
		return (0);
	}
	while (1)
	{
		if (*s == c)
		{
			return ((char *)s);
		}
		if (!*s)
		{
			return (0);
		}
		s++;
	}
	return (0);
}
